#len  测量字典中，键值对的个数
dict01 = {'name':'王志','id':1,'xingbie':'男','address':'河北省沧州市'}
print(len(dict01))
#keys  返回一个包含字典所有key的列表
dict01 = {'name':'王志','id':1,'xingbie':'男','address':'河北省沧州市'}
keys = dict01.keys()
print(keys)
#values 返回一个包含字典所有value的列表
dict01 = {'name':'王志','id':1,'xingbie':'男','address':'河北省沧州市'}
values = dict01.values()
print(values)
#items 返回一个包含所有（键，值）元祖的列表
dict01 = {'name':'王志','id':1,'xingbie':'男','address':'河北省沧州市'}
items = dict01.items()
print(items)
